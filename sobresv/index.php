          Los objetivos de la iniciativa SudVelo son: Garantizar la implementación de rutas ciclistas de alta calidad y de nivel medio en todos los países de SudAmérica, llevando las mejores prácticas a través de las fronteras, hermanando pueblos, disfrutando el paisaje, y armonizando estándares; Comunicar la existencia de estas rutas a los responsables de la toma de decisiones y a los usuarios potenciales, promover y comercializar su uso y proporcionar un importante punto de referencia para obtener información sobre el ciclismo en SudAmérica; De esta manera, animar a un gran número de personas alrededor del mundo a probar la bicicleta y promover así un cambio hacia viajes saludables y sostenibles, tanto para viajes diarios como para cicloturismo.
