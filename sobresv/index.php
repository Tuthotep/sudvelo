          El desarrollo y operación de las rutas SudVelo lo llevan a cabo los gobiernos nacionales, regionales y locales, proveedores de servicios comerciales y ONG's. El establecimiento y mantenimiento de la Mesa Coordinadora SudVelo (CCS) oficial para liderar este trabajo,  con representación en todos los países por los que pasan las rutas SudVelo, es el objetivo de la Asociación, así como representantes en cada una de las provincias por donde transcurre la red.
