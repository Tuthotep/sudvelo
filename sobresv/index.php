          SudVelo es el proyecto de una red, de 5 rutas ciclistas de largo recorrido que cruzan y conectan SudAmérica. Cinco son las rutas del diseño inicial. Las rutas pueden ser utilizadas por cicloturistas de larga distancia, así como por la población local que realiza desplazamientos diarios. Sólo las rutas aprobadas por la Asociacion SudVelo pueden denominar y declarar las rutas que componen la red y/o sus modificaciones.
