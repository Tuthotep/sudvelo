            <h3 class="mt-3 ms-4 text-center">Cruzando las pampas</h3>
            <img src="../news/img/Zarate_Brazolargo_Bridge_Sunrise.jpg" alt="" class="container my-3">
            <p class="lh-base mx-5">
              EuroVelo cycle tourists wishing to discover Greece's rich cultural and natural heritage by bike have a new tool at hand. Find all the information in one place on eurovelo.gr, a new website launched recently by our Greek National EuroVelo Coordinator "Cities for Cycling".
            
              The website is available in English and Greek and offers an overview of the three EuroVelo routes that pass through Greece:

            
              EuroVelo 8 – Mediterranean Route, which meanders along the south-European Mediterranean coast;
            
              EuroVelo 11 – East Europe Route, which leads from Norway's cold Nordkap down to sunny Athens;
            
              EuroVelo 13 – Iron Curtain Trail, an exciting route allowing to explore the history of Europe's division.
            
              There is also plenty of information on how to get there by public transport, including planes, trains, ships and buses. Cycle tourists can even get familiar with the country's national signs. There are additional links to more tourist information (Visit Greece), bike shops, and emergency telephone numbers. Moreover, the website provides general information about the EuroVelo network and offers a news section. It includes videos, weather forecasts, and maps.

            
