            <h3 class="mt-3 ms-4 text-center">La 40 - La ruta Argentina obligatoria</h3>
            <img src="../news/img/ruta40_argentina.jpg" alt="" class="container my-3">
            <p class="lh-base mx-5">
              La Ruta Nacional 40 «Libertador General Don José de San Martín» es la carretera más larga de Argentina.<br>
              Esta ruta turística corre paralela a la cordillera de los Andes, incluyendo tramos cercanos o a través de varios parques nacionales. Es la más larga del país, atravesándolo de sur a norte, y recorre varias de las regiones turísticas y los atractivos más importantes de su territorio. La ruta recorre 5117km; comienza en Santa Cruz, atraviesa 21 parques nacionales, 18 importantes ríos, conecta 27 pasos cordilleranos y trepa (en el km 4601) a casi 5000 m s. n. m. en el abra del Acay en Salta, convirtiéndola en la ruta más alta de América, y también la más alta del mundo fuera de los Himalaya.<br>
              La 40 al una de las rutas en la gestación original del proyecto SudVelo, es parte integrante de la SV1, la "Ruta Andina", en territorio argentino, y es una de las rutas más icónicas y fascinantes para recorrer en bicicleta.<br>
              te. Aquí te dejamos algunos aspectos destacados y consideraciones para hacer la ruta en bicicleta:<br>
              Paisajes Impresionantes: La Ruta 40 atraviesa una diversidad de paisajes asombrosos, desde desiertos áridos hasta altas montañas. Puedes esperar ver glaciares, lagos, cañones, y la majestuosidad de los Andes a lo largo de tu viaje.<br>
              Desafíos de Altitud: La ruta cruza a través de varios pasos de montaña, algunos de los cuales superan los 4.000 metros de altitud. Ten en cuenta los desafíos físicos asociados con la altitud y asegúrate de aclimatarte adecuadamente.<br>
              Condiciones de la Carretera: Aunque gran parte de la Ruta 40 está pavimentada, también encontrarás tramos de ripio y caminos de tierra. Asegúrate de que tu bicicleta esté preparada para diferentes tipos de terreno y lleva repuestos esenciales.<br>
              Clima Variado: Dada la extensión de la ruta, el clima puede variar significativamente a lo largo del recorrido. Desde el frío extremo en la Patagonia hasta el calor en el norte, prepárate con la ropa adecuada y ten en cuenta las estaciones.<br>
              Alojamiento y Abastecimiento: En algunos tramos, especialmente en áreas menos pobladas, puede haber distancias considerables entre servicios. Planifica tus etapas cuidadosamente, asegurándote de tener acceso a alojamiento y alimentos.<br>
              Cultura y Pueblos: La Ruta 40 atraviesa numerosos pueblos y ciudades, cada uno con su propia historia y cultura. Tómate el tiempo para explorar y conocer a la gente local.<br>
              Seguridad: Asegúrate de llevar contigo todo el equipo de seguridad necesario, como casco, luces, y ropa reflectante. También es aconsejable informar a alguien sobre tu itinerario.<br>
              Recorrer la Ruta 40 en bicicleta es una experiencia inolvidable para los amantes de la aventura y el ciclismo. La diversidad de paisajes y la oportunidad de sumergirse en la vasta belleza de Argentina hacen de esta travesía una elección emocionante para aquellos que buscan un desafío en dos ruedas. ¡Prepárate para un viaje lleno de descubrimientos y adrenalina!
            </p>
