              La "Ruta Andina", SudVelo Ruta nº1 (SV1) la primera ruta con la que nace el proyecto, como su nombre indica, transcurre por el lado oriental de la cordillera de los Andes, desde Rio Gallegos - Argentina , hasta Barranquilla - Colombia.
            
              En Argentina, por la mas linda ruta del mundo para hacer en bicicleta. La Ruta Nacional 40.
            
              La Ruta Nacional 40 es una de las rutas más icónicas y fascinantes para recorrer en bicicleta en Argentina. Se extiende a lo largo de la parte occidental del país, recorriendo más de 5.000 kilómetros desde la provincia de Santa Cruz hasta Jujuy, en el norte. 
              
